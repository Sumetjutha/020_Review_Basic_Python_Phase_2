### Tuple

## การแก้ไขข้อมูลชอง Tuple แต่มันแก้ไม่ได้แร้วอ่ะ

##############################################################################
tup = (1,2,3,4,"JohnTik","มะม่วง",True,3.99) # tuple
print("tup => ",tup)

# แปลง tuple ให้เป็น list สิ
lis = list(tup)
lis[0] = "กรุงเทพ"
print("lis => ",lis)

# แล้วก็แปลง list ให้กลายเป็น tuple
tup = tuple(lis)
print("tup ก้อนใหม่ => ",tup)
