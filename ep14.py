### เรียงสมาชิกหลังสุด => หน้าสุด

##############################################################################
fruit = ["มะม่วง","มะยม","มะนาว","ทุเรียน"]
print("list fruit ก่อนการเรียงลำดับสมาชิก => ",fruit)
## หยิบตัวหลังสุดออกมาก่อน
fruit = fruit[::-1]
print("list fruit หลังการเรียงลำดับสมาชิก, จากหลังไปหน้า => ",fruit)


