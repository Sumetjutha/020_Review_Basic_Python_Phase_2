### การใช้ Loop ดึงจำนวนสมาชิก

tup = (1,2,3,4,"JohnTik","มะม่วง",True,3.99) # tuple
print("tup => ",tup)

for item in tup:
    print("สมาชิก => ",item)

print("End")

if True in tup:
    print("เป็นสมาชิก")
else :
    print("ไม่เป็นสมาชิก")

print("End")

