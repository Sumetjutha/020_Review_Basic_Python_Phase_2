print("pet หลังการใช้ remove => ",pet)

### ถ้าใช้ remove แล้วมันไม่อยู่ใน SET มันจะ Error
### ถ้าใช้ discard พอใช้แล้ว มันจะไม่ ERROR
pet.discard("แมวน้ำ")
print("pet หลังการใช้ discard => ",pet)

pet.discard("กระรอก")
print("pet หลังการใช้ discard => ",pet)

