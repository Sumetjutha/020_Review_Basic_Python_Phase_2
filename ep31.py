## การดึงข้อมูลสมาชิกโดยใช้ loop for
pet = {"หมา","แมว","นก","ลิง","กระต่าย","กระรอก"}
print("pet => ",pet)

for item in pet:
    print("สมาชิกของ Set => ",item)

print("End")

