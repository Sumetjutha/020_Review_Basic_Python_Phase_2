### การทำงานร่วมกับ List

tup = (1,2,3,4,"JohnTik","มะม่วง",True,3.99) # tuple
print("tup => ",tup)

city = ["กรุงเทพ","ชลบุรี","อุบลราชธานี"]

tup = tup + tuple(city)
print("หลังการเปลี่ยน city เป็น tuple แล้วเอามารวมกัน => ",tup)

