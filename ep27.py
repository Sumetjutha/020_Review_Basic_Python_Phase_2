### การค้นหาและนับจำนวนสมาชิก 
tup = (1,2,3,4,"JohnTik","มะม่วง",True,3.99,4) # tuple
print("tup => ",tup)

# ใช้ count
tup.count(4)
print("เจออยู่จำนวน => ",tup.count(4),"ตัว")

# ค้นหาสมาชิกด้วย index คือเชคว่าเจอที่ index อะไร
tup.index(4)
print("เจออยู่ตำแหน่ง index ที่ => ",tup.index(4))
