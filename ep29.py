##############################################################################
### reverse tuple
x = (100,20,30,15,10,5,500)
print("x => ",x)

x = reversed(x)
print("x หลัง reverse => ",x)
# มันเป็น object ต้องแปลงกลับมาเป็น tuple ก่อน
x = tuple(x)
print("x หลังเปลี่ยนกลับมาเป็น tuple => ",x)
