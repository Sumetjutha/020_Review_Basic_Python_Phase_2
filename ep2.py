### การแก้ไขข้อมูล
## list เป็นข้อมูลที่เปลี่ยนแปลงได้
number=[1,2,3,4,5,6] 

# ชื่อตัวแปร [index] = "ข้อมูลที่จะทำการแก้ไข"
print("ก่อนแก้ไข => ",number)

number[2] = 9
number[-1] = 10
number[1] = "๋นายไข่"

print("หลังแก้ไข => ",number)
